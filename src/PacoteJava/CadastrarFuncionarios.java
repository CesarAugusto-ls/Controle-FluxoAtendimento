    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String nome = jNome.getText();
        String horentrada = jHorEntrada.getText();
        String horsaida = jHorSaida.getText();

        Funcionarios fun = new Funcionarios();
        fun.setNome(nome);
        fun.setHorEntrada(horentrada);
        fun.setHorSaida(horsaida);
        fun.setID(fun.GerarID());
        fun.setStatus("Ativo");

        Main.Funcionarios.add(fun);

        try {

            File arquivo = new File(Main.Caminho2 + "Funcionarios.txt");

            FileWriter fw = new FileWriter(arquivo, true);
            BufferedWriter bw = new BufferedWriter(fw);
            
            bw.write("" + fun.getID());
            bw.write(";-");
            bw.write("" + fun.getNome());
            bw.write(";-");
            bw.write("" + fun.getHorEntrada());
            bw.write(";-");
            bw.write("" + fun.getHorSaida());
            bw.write(";-");
            bw.write("" + fun.getStatus());
            bw.write(";-");
            bw.newLine();

            bw.close();
            fw.close();
            
            JOptionPane.showMessageDialog(this, "Cadastro Realizado com Sucesso", "Sucesso", JOptionPane.DEFAULT_OPTION);
            dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "NÃ£o foi possivel salvar no TXT", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

